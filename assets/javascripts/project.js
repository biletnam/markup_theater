//****************************************************************************************************
//
// .. READY
//
//****************************************************************************************************
$(function() {
  
  setSectionHeight();
  
  
  
  //****************************************************************************************************
  //
  // .. SCROLL
  //
  //****************************************************************************************************
  $(window).scroll(function() {});



  //****************************************************************************************************
  //
  // .. RESIZE
  //
  //****************************************************************************************************
  $(window).smartresize(function() {
    
    setSectionHeight();
    
  });
  
});



//****************************************************************************************************
//
// .. LOAD
//
//****************************************************************************************************
$(window).load(function() {});



//****************************************************************************************************
//
// .. FUNCTION
//
//****************************************************************************************************
function setSectionHeight() {
  var windowH = $(window).outerHeight();
  $('.section.__full').each(function() {
    $(this).height(windowH);
  });  
}